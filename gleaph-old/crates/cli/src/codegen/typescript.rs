    #[test]
    fn ts_typed_params_emit_native_types() {
        use gleaph_types::PreparedValueType;
        let mut info = make_info(
            "search",
            PreparedKind::Query,
            &[],
            &["name"],
            &[],
            None,
            false,
            "MATCH (u:User) RETURN u.name AS name",
        );
        info.parameters = vec![
            gleaph_types::PreparedParameterInfo {
                name: "min_age".into(),
                required: true,
                types: vec![PreparedValueType::Int64],
                inferred: true,
            },
            gleaph_types::PreparedParameterInfo {
                name: "name".into(),
                required: true,
                types: vec![PreparedValueType::Text],
                inferred: true,
            },
            gleaph_types::PreparedParameterInfo {
                name: "active".into(),
                required: false,
                types: vec![PreparedValueType::Bool],
                inferred: true,
            },
        ];
        let ts = generate_ts(&[info], "abc", JsParamStyle::Camel);
        assert!(ts.contains("minAge: bigint;"), "expected bigint: {ts}");
        assert!(ts.contains("name: string;"), "expected string: {ts}");
        assert!(ts.contains("active?: boolean;"), "expected boolean?: {ts}");
    }

