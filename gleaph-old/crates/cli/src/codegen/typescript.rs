    #[test]
    fn ts_sort_key_names_are_stable_for_digits_and_collisions() {
        let stmts = vec![make_info(
            "find_user",
            PreparedKind::Query,
            &[],
            &["name"],
            &[
                ("1day", "u.one_day"),
                ("user_name", "u.user_name"),
                ("user-name", "u.alt_user_name"),
            ],
            None,
            false,
            "MATCH (u:User) RETURN u.name AS name",
        )];
        let ts = generate_ts(&stmts, "abc", JsParamStyle::Camel);
        assert!(
            ts.contains("  /** wire key: \"1day\", expr: \"u.one_day\" */\n  K1day: \"1day\",")
        );
        assert!(ts.contains(
            "  /** wire key: \"user_name\", expr: \"u.user_name\" */\n  UserName: \"user_name\","
        ));
        assert!(ts.contains("  /** wire key: \"user-name\", expr: \"u.alt_user_name\" */\n  UserName2: \"user-name\","));
    }

    #[test]
    fn ts_optional_params_are_optional_in_interface() {
        let mut info = make_info(
            "search",
            PreparedKind::Query,
            &[],
            &["name"],
            &[],
            None,
            false,
            "MATCH (u:User) RETURN u.name AS name",
        );
        info.parameters = vec![
            gleaph_types::PreparedParameterInfo {
                name: "query".into(),
                required: true,
                types: vec![],
                inferred: false,
            },
            gleaph_types::PreparedParameterInfo {
                name: "offset".into(),
                required: false,
                types: vec![],
                inferred: false,
            },
        ];
        let ts = generate_ts(&[info], "abc", JsParamStyle::Camel);
        assert!(ts.contains("export interface SearchParams {\n  /** wire param: \"query\" */\n  query: unknown;\n  /** wire param: \"offset\" */\n  offset?: unknown;\n}"));
        assert!(ts.contains("graph.executePrepared(\"search\", { query: params.query, ...(params.offset !== undefined ? { offset: params.offset } : {}) })"));
    }

